    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        if (txtReporteMostrado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe de seleccionar el reporte a exportar");
            return;
        }
        //Si el campo no esta vacio, se exporta a un archivo HTML el reporte mostrado

        archivosPrograma.exportarReporteHTML(txtReporteMostrado.getText());
    }//GEN-LAST:event_btnExportarActionPerformed
    private void mostrarPartidas() {
        int tamanio = listaPartidas.obtenerSize();
        StringBuilder sb = new StringBuilder();
        
        for (int i = 0; i < tamanio; i++) {
            try {
                sb.append(i).append(": ").append(listaPartidas.obtenerValor(i).getNombreDePartida()).append("\n");
            } catch (ListaException ex) {
                Logger.getLogger(VentanaTiposDeReporte.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        txtPartidasDisponibles.setText(sb.toString());
    }

