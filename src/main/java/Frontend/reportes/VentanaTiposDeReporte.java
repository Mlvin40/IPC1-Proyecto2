    private void btnMostrarElegidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarElegidoActionPerformed
        
        try {
            int seleccionado = Integer.parseInt(txtSelected.getText());
            
            if (seleccionado < 0 || seleccionado >= listaPartidas.obtenerSize()) {
                JOptionPane.showMessageDialog(null, "Elija un reporte valido");
                return;
            }
            
            txtReporteMostrado.setText(listaPartidas.obtenerValor(Integer.parseInt(txtSelected.getText())).mostrarReportes());
            
            txtSelected.setText("");
        } catch (ListaException ex) {
            Logger.getLogger(VentanaTiposDeReporte.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "debe seleccionar una partida valida");
        }
    }//GEN-LAST:event_btnMostrarElegidoActionPerformed

